    	<div class="projecttext">
        <p>Fontys had een evenement georganiseerd genaamd Hackathon. Hierbij ga je 24 uur aan een opdracht werken. Wij hadden als opdracht om een datavisualisatie te maken voor de MirrorRoom van We Are Data.</p>
        </div>
                 <div class="titeluitleg">
            <span>
            <h3>UITLEG</h3>
            </span>
            </div>
            
    	<div class="projecttext">


        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
            <dt class="gallery-icon landscape">

        <img src="../images/hackathon1.jpg"class="attachment-thumbnail" height="548" width="958">
            </dt></dl>
        </div>

        <p>De Hackathon werd gehouden in de Effenaar in Eindhoven. Om 13.00 uur begon de kick-off. Daarbij kregen we onze opdracht te horen en in welke groepje we zaten. Omdat niet iedereen was opkomen dagen, ben ik bij een ander groepje gaan zitten. We zijn als eerste de data gaan uitlezen die we hadden meegekregen. We kregen een CSV en een JSON bestand. Uiteindelijk kwamen we erachter dat er in de JSON bestand een error zat. Hierdoor moesten we op wachten tot we een verbeterede versie kregen. Uiteindelijk kregen we die en gingen we brainstormen welke onderwerpen ons interessant leek om op te filteren. We vonden het interessant om ons te richten op het geslacht. Bijvoorbeeld hoeveel vrouwen er vergeleken met mannen de MirrorRoom bezochten. De onderwerpen die ons interessant leken waren het verschil tussen mannen en vrouwen of ze hun data gingen publiceren, wat hun grootste angst was, de locatie en gewicht.</p>

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
            <dt class="gallery-icon landscape">

        <img src="../images/hackathon2.png"class="attachment-thumbnail" height="548" width="958">
            </dt></dl>
        </div>


        <p>Ik had al een keer een datavisualisatie gemaakt die de verschillen liet zien tussen mannen en vrouwen voor een schoolproject. Dit leek mij wel leuk om dit te gaan verbeteren en uit te werken op basis van deze data. Met behulp van Javascript roep ik de JSON bestand op. Hierin staan alle data van de MirrorRoom. Deze gegevens worden getoond door middel van een percentage die wordt berekend met JQuery. Daarna heb ik de visualisatie erbij gemaakt. De poppetjes had ik in het vorige project al uitgetekend. Nu heb ik er een mooie polygon afbeelding achter de poppetjes gezet. Deze stijl paste goed bij We Are Data. De polygon afbeelding heb ik daarna natuurlijk in het roze en blauw gemaakt waardoor je het verschil tussen man en vrouw goed zag. De afbeelding werd door middel van JQuery naar boven geanimeerd waardoor je een mooie animatie kreeg. Ik heb ook een menu er bij gemaakt waarmee je kon switchen naar een andere onderwerp. We hadden twee onderwerpen uitgewerkt. Dit was het verschil van de opkomst tussen mannen en vrouwen en het verschil wie er hun profiel had gepubliceerd tussen mannen en vrouwen. <a href="http://i315962.iris.fhict.nl/hackathon/dv.html">Hier</a> kan je naar de datavisualisatie gaan.</p>

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
            <dt class="gallery-icon landscape">

        <img src="../images/dv.png"class="attachment-thumbnail" height="548" width="958">
            </dt></dl>
        </div>

        <p>Tussendoor ben ik nog in de MirrorRoom geweest. Dit was erg interessant. Ik wist al wel van tevoren wat er ging gebeuren, dus het was minder spannend. Verder vond ik het heel goed gemaakt en was het interessant om te zien wat voor data de MirrorRoom van jou kan zien. </p>

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
            <dt class="gallery-icon landscape">

        <img src="../images/hackathon5.jpg"class="attachment-thumbnail" height="548" width="958">
            </dt></dl>
        </div>

        <p>Ik ben tot 20.00 uur gebleven om de datavisualisatie te maken. Van ons groepje waren er nog maar drie over. De andere twee bleven ook niet meer en heb ik gekozen om ook naar huis te gaan.</p>

        <p>De volgende ochtend ben ik weer naar de Effenaar gegaan. We hadden nog aan de datavisualisatie gewerkt. De opkomst van de Hackathon was nog kleiner als gisteren. Daarom zijn we met een aantal groepjes bij elkaar gaan zitten en een groep gaan vormen. We hebben elkaars werk laten zien en gekeken wat er overeenkwam. Iedereen was er mee eens om meer te verdieppen tussen mannen en vrouwen. Iedereen maakte een datavisualisatie met een andere onderwerp van het verschil tussen mannen en vrouwen. Mijn datavisualisatie had ik ook nog een beetje aangepast. Iedereen vond de datavisualisatie visueel heel mooi uitzien.
        <br>
        Daarna kwamen de juryleden en gingen ze langs elk groepje. De jury was onder de indruk van het visueel aspect van mijn datavisualisatie. Als groepje moesten we daarna presenteren. Hierbij lieten wij al onze datavisualisaties zien. Jammer genoeg hadden we geen prijs gewonnen.</p>

