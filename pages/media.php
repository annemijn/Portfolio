        <p>Toen we deze diagram aan het invullen waren, dachten we eraan om een campagne stunt te doen. hierdoor kunnen we veel mensen aanspreken. We dachten aan een mini event bij een hogeschool. Hierbij zal er een kleine tent op het gras worden gezet waarin je gratis drankjes krijgt en klassieke muziek hoort. Met dit event kunnen we dan een promovideo maken voor social media en op de website. Door dit allemaal uit te denken kwamen we uit op de customer journey. Hier staat welke media en hoe we dit gaan gebruiken bij de verschillende fases van de journey. Deze fases zijn awareness, consideration en engage. Met dit concept komen we tot engage, verder zullen we andere fases niet bereiken. We hebben elke media van het vorige diagram in de customer journey gezet. Bij sommige media hebben we uitgelegd op welke manier we die gaan gebruiken. Zo heb je bijvoorbeeld een social media share/like actie en videos vanaf youtube of de Facebook pagina. Dit hebben we op volgorde gezet zodat de route ernaar toe helemaal klopt.</p>
       

