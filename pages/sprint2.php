        <p>Daarna zijn we taken gaan verdelen wat we deze week af wilden hebben. Ik had als taken gekozen om de huisstijldocument te maken en verschillende opties voor de logo te maken.<br>
        In het huisstijldocument zette ik de kleuren met de kleurcodes in. Daarna zette ik de fonts die we willen gebruiken. Hierbij koos ik voor een dikgedrukte header die goed opvalt. Daarnaast een lichte condensed body tekst die goed leesbaar is.
        </p>

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
