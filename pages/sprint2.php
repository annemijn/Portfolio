        <p>Ik vond dit een goede voorbeeld voor onze concept. Deze huisstijl gaf veel variatie voor de logo, achtergronden en lettertype. Zo kan je verschillende patronen gebruiken voor de logo of als opvulling van de lettertype. Om dit uit te voeren moesten we definitieve kleuren gaan uitkiezen. Iedereen deed afzonderlijk kleuren uitzoeken. We lieten aan elkaar de kleuren zien de we hadden uitgekozen. We kwamen erop uit dat we mijn kleuren gingen gebruiken. Ze vonden het mooie kleuren die goed bij het festival passen.</p>
       

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
