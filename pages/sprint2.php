        <p>Dinsdag heb ik ’s ochtends de contentstrategie gemaakt en heb ik de customer journey aangepast. ’s Middags kregen we les van DEV. Daarbij kregen we de opmerking dat we nog iets technisch moesten verzinnen naast de website die we hebben gemaakt. We merkte dat we niet op iets kwamen en dat de leraar het ook heel lastig vond. Morgen zullen we hier verder met het groepje overleggen, omdat alleen Laura en ik er waren. <br>
        Woensdag ben ik de middelen gaan onderbouwen die we hebben gebruikt voor de customer journey en de contentstrategie. Dit hebben we daarna in een document gezet met daarbij de customer journey en de contentstrategie. De proces en de resultaten hiervan kan je <a href="media.php">hier</a> zien.</p>

        <p>We hebben met onze product owner nog een retrospective gehouden. Dit hadden we namelijk nog niet gedaan na de sprintdemo. We kregen hierbij nuttige feedback. Hij wilde dat wij interviews gaan doen met studenten bij de TU. Zo kunnen we straks bij de sprintdemo met deze resultaten laten zien dat het concept aanspreekt bij de doelgroep. Dit kunnen wij het beste doen met lekker weer wanneer de studenten buiten zitten. Ook vroegen we nog aan de product owner of we nog iets technisch voor het concept moesten maken. Hij had een idee om een datavisualisatie te laten zien op het festival. Hierbij kan je werken met licht en data van de bezoekers die er op dat moment zijn. Hij zij wel dat dit nu niet het belangrijkste is en dat we dit nu even moeten laten liggen. We moesten ons meer gaan focussen op het verantwoorden van ons concept. Daarna hebben we besproken wat we af willen hebben voor de volgende sprint demo. Zo willen we de interviews volgende week gaan houden. Er is goed weer voorspeld, dus dit leek ons een goed moment. Verder willen we dan ook als het lekker weer is onze campagne stunt uitvoeren. Hiervoor moeten we nog wel voorbereiden en opstellen wat voor spullen we moeten meenemen. Ik had daarbij wel een opmerking hoe we ooit zoveel spullen krijgen. Ik dacht eraan om het juist wat kleiner te houden, met een aantal zitzakken en wat drinken, waarmee we toch nog de sfeer kunnen laten zien. Dit vonden de andere groepsleden ook een goed idee. Maandag zullen we gaan voorbereiden op de campagnestunt en vragen gaan opstellen voor de interviews.</p>

        <p>Die week daarna kregen we een workshop van UX. Dit ging over usabillity testen en moesten we een testplan gaan maken. Volgende week zullen we de test gaan uitvoeren. De resultaten kan je hier zien. We zijn daarna gaan nadenken welke spullen we nodig hebben voor de campagnestunt. Deze willen we namelijk woensdag gaan uitvoeren. Hiervoor hebben we een lijstje gemaakt. Zo willen we zitzakken regelen, een camera, eten/drinken, iPad voor de website en boxen voor muziek. Deze spullen zullen we woensdag meenemen. Met het groepje zijn we daarna interviewvragen gaan opstellen. We willen namelijk ook bij de campagnestunt studenten gaan interviewen. ’s Middags kregen we les van SCO. Deze les ging over semiotiek. Hiervoor heb ik een poster serie gemaakt. De posters kan je <a href="semiotiek.php">hier</a> zien. Daarna zijn we wat taken gaan verdelen. Ik ging een ticketsysteem implementeren in de website en een poster/flyer maken voor de campagnestunt. De ticketsysteem had ik gemaakt bij de DEV workshop waarbij we een framework zelf moesten schrijven. Dit kan je <a href="framework.php">hier</a> zien. ik heb de ticketsysteem nu in de stijl gemaakt van ons concept. Ook heb ik het nu werkend gekregen dat je wordt doorgestuurd naar de website van ticketmaster als je alles goed heb in gevuld.  </p>

        <p>Dinsdag heb ik aan mijn groepsleden het aangepaste ticketsysteem laten zien. Ze vonden hem er goed uitzien. Sam zal deze toevoegen aan de website, zodat we dit ook kunnen testen met UX. Daarna ben ik nog een aantal posters gaan maken voor de campagnestunt. Morgen zullen we kiezen welke poster we gaan uitprinten. ’s Middags heb ik een workshop gehad van DEV. Dit ging over SEO. Dit kan je <a href="seo.php">hier</a> lezen.</p>


