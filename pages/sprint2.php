            <h3>REFLECTIE</h3>
            </span>
        </div>

        <p>Ik vind dat we als groep heel veel hebben gedaan in sprint 2. We zijn allemaal druk bezig geweest om onze campagne te maken. Zo heb ik met Bram een duidelijke communicatie, media- en contentstrategie gemaakt. Met de feedback van de SCO lerares heb ik veel geleerd over hoe dit document in elkaar zit. Deze sprint ging ook vooral om prototypes en het testen daarvan. Één van de belangrijkste prototypes was de campagnestunt. Dit was wel het leukste onderdeel die we deze sprint hadden gedaan. Het was een heel belangrijk onderdeel van ons concept en we hebben er veel uitgehaald. Zelf heb ik nog bij de website een usabillity test gedaan waar we veel aan hebben gehad. Dit was erg fijn om de website gebruiksvriendelijk te maken.</p>
        
        <p>De stijl bepalen van ons concept ging deze sprint heel lastig. Iedereen had posters gemaakt en huisstijlen ontworpen, maar we kwamen maar niet op één lijn terecht. Hierdoor kwamen we aan het einde van de sprint in de problemen. Het was heel lastig om dit door te voeren in de website en andere producten, omdat we dus geen een stijl hadden gekozen.</p>
        
        <p>Ook merkte ik dat we aan het einde van de sprint de pitch een beetje afraffelde. Hiervoor hadden we niet genoeg tijd voor uitbesteed. Er moesten nog op het laatste moment dingen in de pitch gezet worden. Dit vond ik wel erg jammer.</p>

        </p><br>

