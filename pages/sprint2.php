        <p>We kozen voor deze kleuren omdat ze een warme uitstraling geven en vrolijkheid uitstraalde. Dit vonden we goed bij een chill festival passen. <br>
        Daarna zijn we voorbereidingen gaan treffen voor de campagnestunt. Ik maakte de taken en verdeelde die over de groep. Robin en ik gingen de camera, statief en de zitzakken regelen. Dit hadden we bij de ISSD gehaald en de zitzakken mochten we meenemen die in de openbare ruimte lagen. Toen we al spullen bij elkaar hadden gingen we naar buiten om alles op te zetten. Hoe alles verliep en wat de resultaten waren kan je hier lezen bij UX.</p>

        <p>Maandag zijn we met UX de usabillity test gaan uitvoeren voor de website. Dit kan je <a href="stunt.php"></a> lezen. Vorige week woensdag hadden we onze campagnestunt uitgevoerd. Jammer genoeg zijn we al onze beelden kwijt die op de camera stonden. We hebben wel nog een paar foto’s en de audio van de interviews. We zijn toen de interviews gaan analyseren. <br>
        Daarna zijn we nog gaan nadenken over een digitaal middel voor de campagne. Als idee had ik om met gezichtsherkenning een visualisatie te maken. Met licht kun je verschillende kleuren gebruiken. Elke kleur zal een emotie voorstellen. Zo heb je geel voor blij, blauw voor verdrietig, rood voor boos enz. Dit zal in een donkere ruimte worden afgespeeld. Bijvoorbeeld in een tent. Hierbij zullen veel led lichtjes in de tent worden gebruikt op het plafond en aan de zijkanten. deze led lichtjes zullen verschillende groottes hebben. Er zullen ook geluiden bij worden gebruikt. Zo heb je verschillende tonen die bij emoties worden gebruikt. hoge toon bij blij en lage toon bij verdriet. In de tent zie je voor elke activiteit een beeldscherm. hierop zie je de camera die de activiteit filmt. Rondom elke beeldscherm zullen de lichten en geluid zijn die de emoties van de activiteiten weergeven. Anderen vonden dit ook een leuk idee.<br>
        ’s middags kregen we feedback van de content- en mediastrategie die we vorige week hadden ingeleverd. De feedback en hoe we het hebben verbeterd kan je <a href="media.php">hier</a> lezen. </p>

        <p>Daarna zijn we nog gaan nadenken over het digitaal middel. De SCO docent had hier bij de feedback ook over dat we wel nog een digitaal middel moeten maken voor de campagne. We kwamen erop uit dat we als visualisatie gebruik willen maken van bellenblaas. Deze zal bij elke activiteit staan. de camera zal de emoties van de gezichten zien. Als er meerdere mensen lachen, zal de bellenblaas aan gaan. Hoe meer mensen lachen, hoe meer bellen er komen. Op deze manier kunnen mensen op het festivalterrein zien als het erg leuk is bij een activiteit.<br>
        Daarna hadden we nog mijn idee besproken en willen we op een scherm een datavisualisatie laten zien. In deze visualisatie zal er worden getoond welke emoties er aanwezig zijn bij de activiteit. Zo zie je hoeveel mensen blij, boos, verdrietig, angst en walging zijn.
        </p>

        <p>Dinsdag heb ik het digitaal middel uitgeschreven. Hiermee hebben we dit duidelijk op papier en kunnen we het in de pitch vertellen. Verder hebben we aan de DEV leraar feedback gevraagd over het digitaal middel. Hij vond het een leuk idee en gaf een aantal ideeën hoe we dit konden uitwerken. Daarna ben ik aan de slag gegaan met het verbeteren van de content en media strategie. Daarbij heb ik onderzoek gedaan hoe je volgers krijgt op Facebook en Instagram. Dit heb ik helemaal uitgeschreven. Verder hebben we nog een lijstje gemaakt met taken wat we nog meer moeten verbeteren aan het document. Deze taken zullen we morgen verdelen en gaan uitvoeren.<p>

        <p>Woensdag kregen we feedback van onze product owner. Hij wilde dat we verschillende stijlen gingen maken van de website en dat we dit moesten opslaan voor onze portfolio, zodat we een goede proces hebben gemaakt. Hij vond dat we in de video van de campagnestunt infographics moesten neerzetten van de resultaten van de interviews. Hierdoor krijg je een goed overzicht.<br>
        Daarna ben ik samen met Bram de content-en mediastrategie document gaan verbeteren op basis van de feedback van de docent. We hadden taken verdeeld en zijn het document gaan verbeteren. In overleg samen hadden we een groot deel verbeterd. Volgende week zullen we hiermee verder gaan.</p>



