    	<div class="projecttext">
        <p>In week 10 vond de tweede demo plaats bij Perplex. Daar moesten we onze gekozen concept presenteren en laten zien welke media middelen we gaan gebruiken. Bram en Arjan gingen deze keer presenteren. Als feedback kregen we te horen dat we nog geen duidelijke identiteit aan onze festival hadden gegeven. Ze vonden dat de doelgroep die we wilde aanspreken duidelijker moest worden gemaakt. Zo kan je daarop meer focussen bij je festival. Verder vonden ze de campagne stunt wel leuk, maar wisten ze niet of het wel ging werken. Ze wilde graag dat dit door ons word uitgevoerd. Met deze feedback gaan we morgen aan de slag om te beslissen wat we voor deze sprint gaan doen.<br>
Woensdag zijn we een retrospective gaan doen. Dit kan je <a href="scrumsprint2.php">hier</a> lezen. Daarna zijn we de taken gaan verzinnen die we deze week af wilde hebben. Samen willen we een duidelijke huisstijl hebben met een definitieve logo, kleuren en fonts. Als inspiratie kwam Sam op deze huisstijl van een festival.
</p>
        

