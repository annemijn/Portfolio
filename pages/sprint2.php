        <p>Daarna heb ik deze patronen op de logo toegepast. Ik vond dat je een mooie variatie kreeg van logo’s die toch goed bij elkaar paste. Het concept heeft nu een eigen stijl.<p>

        <p>De week erop kregen we een workshop van UX waarbij we prototypes gingen maken. Dit kan je <a href="prototype.php">hier</a> lezen. ’s Middags kregen we les van SCO. Daarbij kregen we les over de contenstrategie. Ook kregen we feedback over onze customer journey. Het proces hiervan kan je <a href="media.php">hier</a> zien. Na de les zijn we over de identiteit van het concept gaan nadenken. We wilde een USP creëren voor ons concept. Daarbij kwamen we eruit dat het chillen en weg van de dagelijkse stress ons belangrijkste doel is. Dit willen wij ook het belangrijkste maken voor ons festival. Rondom deze USP zijn we activiteiten gaan verzinnen op het festival dat te maken heeft met de USP. Zo kwam ik op een aantal activiteiten zoals, een zandgebied waarop je het gevoel krijgt alsof je op het strand ligt, schilderen op de rustige muziek waarmee je helemaal tot rust komt, bellen blazen/maken, en massagestoelen. Dit hebben we in onze concept document gezet.
        </p>

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-item">
            <dt class="gallery-icon landscape">

