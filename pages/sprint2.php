        <p>Ook maakte ik een aantal opties voor de logo. Ik ging opzoek naar verschillende patronen die ik kon gebruiken. Ik had een aantal patronen gevonden die seamless waren. Dit was makkelijk om te gebruiken. De patronen heb ik in de kleuren van de huisstijl gemaakt</p>

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
            <dt class="gallery-icon landscape">

        <img src="../images/logospatterns.png"class="attachment-thumbnail" height="548" width="958">
