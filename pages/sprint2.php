        <p>Woensdag kregen we ’s ochtends van onze product owner feedback over wat we hadden gedaan afgelopen week. We hadden uitgelegd dat we bezig waren met onze huisstijl, posters maken, website verbeteren en het verzinnen van een tweede technische middel. Verder legde ik uit dat we vandaag bezig zijn met het voorbereiden van de campagnestunt en dat we dit vanmiddag willen gaan uitvoeren. Hier waren ze enthousiast over. Verder wilde hij nog de website en de posters zien. hij vond het goed dat wij verschillende stijlen zijn uit gaan proberen. Wel vond hij dat we niet te lang moesten zitten op verschillende stijlen uitproberen, maar dat we een stijl moesten gaan kiezen. <br>
        We zijn daarna ons huisstijl voor de tweede keer gaan kiezen. De kleuren die we daarvoor hadden gebruikt vonden we te fel en paste niet goed bij elkaar. Hier kwamen we erachter toen we de posters gingen maken en de website ging veranderen. Het kiezen van de huisstijl ging dit keer wat makkelijker dan vorige keer omdat we graag progress wilde maken. </p>

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
            <dt class="gallery-icon landscape">

        <img src="../images/sprint2huisstijl2.png"class="attachment-thumbnail" height="548" width="958">
