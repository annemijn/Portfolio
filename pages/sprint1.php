            <h3>REFLECTIE</h3>
            </span>
        </div>

        <p>Voor deze sprint heb ik vooral gewerkt aan de campagnestunt en het design aspect van het concept. Met de campagnestunt heb ik verzonnen om een mini festival te creëren op een schoolplein. Het was heel leerzaam om hier met de groepsleden over te discussiëren. Verder was het leerzaam om hierop feedback te krijgen van de docenten. Ik heb de stunt uitgelegd aan Bram en Hanna. Van hun kreeg ik bruikbare feedback. Zo gaf Bram een inspiratievoorbeeld van een documentaire op Netflix. Deze gingen we allemaal kijken. Deze documentaire ging over een podiumontwerpster die concerten designed. Verder vond Bram het een goed idee om dit een keer uit te proberen bij het TU terrein. Dit willen wij ook graag in de tweede sprint doen. <br>
Deze sprint heb ik ook veel aan design gedaan. Zo heb ik een huisstijl document gecreëerd. Met stage heb ik dit ook al een paar keer moeten doen, dus kon ik de ervaring hierop toepassen. Verder vond ik het fijn om feedback te krijgen van mijn groepsleden over het document. Met het poster design heb ik veel geleerd. Zo weet ik hoe andere festival posters eruit zien en wat voor design ze gebruiken. Het viel me op dat ze vaak een getekende stijl aanhouden. Verder vond ik het leerzaam om weer met Photoshop te werken om deze stijl van de poster te creëren. Het was lastig om het patroon te maken en om passende kleuren toe te passen. Ik ben erg blij met het resultaat.
</p><br>
