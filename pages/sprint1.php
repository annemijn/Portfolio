        <p>Daarna ben ik fonts gaan zoeken voor de headers en body tekst. Als inspiratie ben ik gaan kijken naar andere festival websites. Daar kwam ik achter dat ze vaak bold headers gebruiken in hoofdletters. Ik ben gaan kijken bij Google Fonts of ik een mooie lettertype erbij zag. Daar kwam ik uit op de Anton font. Dit was een dikgedrukte lettertype die goed opviel. Als body tekst wilde ik juist het tegenovergestelde hebben. Een font dat condensed en fijn te lezen is. Helvetica light condensed paste hier goed bij. De fonts heb ik bij het document neergezet.
        </p>

