        <p>Met de groepsleden heb ik besproken wat ze van deze kleuren vonden. Ze vonden de kleuren goed bij het concept passen en wilde deze graag gaan gebruiken. Deze heb ik in een document gezet met kleurcodes erbij.</p>

         <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
