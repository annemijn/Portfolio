        <p>De week erop, na de Hackathon, kregen we bezoek van een violist van het Gelders Orkest. Het was erg interessant om te horen hoe een lid van het orkest alles meemaakt. Verder hadden we nog twee workshops gekregen van UX. <a href="ux.php">Hier</a> heb ik uitgelegd waarom ik een goede ux designer ben en wat ik nog wil ontwikkelen als ux designer. Verder heb ik nog een prototype van een <a href="bordspel.php">bordspel</a> gemaakt en van de <a href="website.php">website</a> van de proftaak.</p>

        <p>’s Middags hadden we tijd om taken te verdelen. We hadden overlegd dat iedereen een eigen prototype voor de website gaat maken. Zo kunnen we morgen bespreken welke website we kiezen of combinaties van websites. 
        </p>

        <p>Dinsdag lieten we de prototype van de website aan elkaar zien. uiteindelijk hebben we gekozen om de indeling van mijn ontwerp te kiezen, de header van Sam te gebruiken en de kleuren van Arjan te kiezen. Daarna zijn we taken gaan opstellen. Daar kwamen we achter dat we nog een campagnestunt voor de presentatie van volgende week willen uitwerken. We hadden hiervoor al gebrainstormd. Daar kwam uit dat we een mini event van het concept willen uitvoeren bij een hogeschool. Hierbij zal er een kleine tent op het gras worden gezet waarin je gratis drankjes krijgt en klassieke muziek hoort. Daarbij wilde we ook een promovideo doen. Ik kwam voor de video op het idee om student op een plein van een campus een drankje aan te bieden. Na een tijdje krijgt die een hapje aangeboden. Daarna word er een zitzak, hangmat en tent boven hem neergezet. Iemand zet een speaker neer en daar komt klassieke muziek uit. Je ziet hem op het laatst dat hij er helemaal chill van wordt en lekker kan uitrusten. De andere groepsleden vonden dit een goed idee en we kwamen samen op de campagnestunt. Ik heb daarna de campangestunt uitgeschreven in een document. </p>

        <p>Woensdag zijn we de taken gaan verdelen wat we voor de pitch moesten maken. Bram en Arjan gaan volgende week presenteren. Ik heb als taak gekregen om een huisstijl te maken voor het concept. Hierbij wilde ik een aantal kleuren opzoeken die bij elkaar passen en een paar fonts voor de tekstgebruik. Op color adobe ben ik gaan kijken naar mooie kleurcombinaties die bij ons concept passen. Hierbij kwam ik op deze uit:</p>

