            <h3>REFLECTIE</h3>
            </span>
        </div>

        <p>Ik vond het heel nuttig om deze prototype stunt uit te voeren. Dit is vooral leerzaam voor de volgende keer als je weer zoiets wilt uitvoeren. Zo merkte we dat er niet veel mensen op kwamen dagen omdat meer leek alsof we daar gewoon aan het chillen waren dan dat het uitnodigend was voor andere mensen. Het beste was om een bord met de naam van het festival neer te zetten met daarbij de tekst “kom hier chillen met gratis eten en drinken”. Dit was meer uitnodigend geweest. Verder vond ik het ook heel leuk om te doen. het was een gezellige sfeer ik heb nieuwe mensen leren kennen. Je merkte ook dat de band in onze groep beter werd. Verder vond ik het ook nuttig voor ons concept, want we kunnen met deze stunt ons concept goed onderbouwen. </p>

        <p>Ik heb ook twee interviews gedaan waar ik weer wat van heb geleerd. Ik merk dat ik het steeds minder erg vind om het te doen. Bij het interviewen merkte ik dat ik veel rustiger was dan vorige keer. Ik gaf de mensen de tijd om over hun antwoord na te denken en te antwoorden. Hierdoor kreeg je al meteen een fijne sfeer bij het interviewen.  
        </p><br>

                 <div class="titeluitleg">
            <span>
            <h3>UITLEG</h3>
            </span>
            </div>
            
    	<div class="projecttext">
        <p>We wilden graag de campagnestunt uitvoeren om te kijken of mensen van onze doelgroep hier behoefte aan hebben. Als eerste zijn we voorbereidingen gaan treffen. Met de groep gingen we eerst de vragen verzinnen die we voor de interviews wilde gaan stellen. Het leek ons een goed idee om een aantal mensen die bij de stunt gaan zitten ook te vragen wat ze van het festival vinden. Daarna maakte ik de taken en verdeelde die over de groep. Robin en ik gingen de camera, statief en de zitzakken regelen. Bram en Sam regelde het eten en drinken. De spullen die wij moesten regelen hadden we bij de ISSD gehaald en de zitzakken mochten we meenemen die in de openbare ruimte lagen. </p>

        <p>Toen we al spullen bij elkaar hadden gingen we naar buiten om alles op te zetten. We gingen naar het plein bij de andere gebouwen op Fontys en legde alle spullen neer. Ik zette de camera op de statief zodat we gemakkelijk mensen kunnen filmen. Er kwamen al gelijk twee jongens op ons af. Ze vonden het er chill uitzien en wilde wel graag wat gratis eten en op de zitzakken zitten. Sam interviewde deze jongens over wat ze van het festival vonden. Verder hadden we totaal 8 mensen op onze campagnestunt gekregen. We hebben ze gratis eten en drinken aangeboden en ze laten chillen op de zitzakken. Iedereen die we hadden geïnterviewd waren wel enthousiast over het festival en wilden er wel heen. </p>

        <p>Ik heb twee mensen geïnterviewd. Ik vond het leuk om dit te doen en ik merkte ook dat ik me veel meer ontspannen voelde dan bij de vorige interviews die ik had gedaan. Ik had nuttige informatie gekregen van deze interviews waar we veel aan zullen hebben. Zo kunnen we onderbouwen dat de doelgroep geïnteresseerd is in de stunt en het festival zelf. Jammer genoeg zijn we de beelden kwijtgeraakt van de interviews. Wel heb ik de audio opgenomen.</p>

