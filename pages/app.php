        <p>Met deze workshop heb ik geleerd om user stories  bij een prototype te bedenken en daarbij librarys te verzinnen. Ik vond dit een hele fijne manier. Zo kan je meteen te weten komen welke librarys erg handig zijn om te gebruiken. Dit kost minder tijd en werkt (meestal) meteen. Ook heb ik geleerd om een plugin van JQuery te gebruiken. Ik vond dit makkelijker gaan dan verwacht. In de documentatie was duidelijk aangegeven waar de belangrijkste punten kon aanpassen. Wel vond ik dat ik hierdoor niet meer echt de code zelf leerde begrijpen. Je gaat gemakkelijk de code die je niet wilt aanpassen overslaan. Hierdoor kan er ontstaan dat je errors niet kan verhelpen doordat je de code niet begrijpt. </p><br>

