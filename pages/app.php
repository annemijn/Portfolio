        <div class="projecttext">
        <p>Voor het vak Dev gingen we met het groepje een prototype bedenken voor het concept. Wij hadden hier al over nagedacht en wilde graag een website maken. Dit gingen wij schetsen op het bord om de indeling van de website te laten zien. </p>

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
            <dt class="gallery-icon landscape">

        <img src="../images/app2.png"class="attachment-thumbnail" height="548" width="958">
            </dt></dl>
        </div>

        <p>Daarna moesten we een aantal user stories bedenken bij de prototype. Hierbij ging het om wat de gebruiker graag wilt op de website. We hadden veel user stories bedacht. Hierbij wat voorbeelden:</p>

        <p>-    Als gebruik wil ik een datum zien zodat ik weet wanneer het festival is. <br>
            -   Als gebruiker wil ik de prijs van de tickets zien zodat ik weet hoeveel geld ik moet uitgeven.<br>
            -   Als gebruiker wil ik het programma hebben van het evenement zodat ik weet wat er te doen is.<br>
            -   Als gebruiker wil ik een beeld krijgen van het festival zodat ik overgehaald kan worden om deze te bezoeken.<br>
            -   Als gebruiker wil ik een overzicht te zien krijgen van de verschillende tickets zodat ik een geschikte ticket kan kopen.</p>
        
        <p>Nadat we de userstories hadden verzonnen, moesten we een library bij een user story koppelen. Door deze librarys te verzamelen kan je straks gemakkelijker de website maken zonder teveel zelf te programmeren. Ik pakte de user story: Als gebruik wil ik een datum zien zodat ik weet wanneer het festival is. Hierbij was ik op zoek naar een count down. Dit laat zien hoelang het nog duurt voordat het begint. Ik kwam al gauw op de library van jQuery die een plugin had genaamd “the final countdown”. Dit is een duidelijke count down waarmee je snel eentje kan neerzetten. Dit scheelt veel programmeerwerk. Op de website staat er duidelijke documentatie om met de plugin te werken. Dit maakt het makkelijker om code aan te passen. Ik heb de plugin geïnstalleerd en zelf een count down gemaakt. </p>

<div id="getting-started"></div>
<script type="text/javascript">
  $("#getting-started")
  .countdown("2018/06/08", function(event) {
    $(this).text(
      event.strftime('%D dagen %H uur: %M minuten: %S seconden')
    );
  });
</script>

        <p>Wat me opviel is dat ik best makkelijk de count down werkend kon maken. Verder merkte ik wel dat ik de code niet altijd goed begreep. In de documentatie staat het lastig uitgelegd en snapte ik een aantal termen niet. Als je al een aantal termen niet begrijp, weet je niet hoe de code in elkaar steekt. Daarom dat ik het ook best lastig vond om het uitgebreid aan te passen. Wat wel makkelijk was is de tijd aan te passen. Je moet de datum intikken waarop het evenement plaats vind. Hiervan berekend die hoeveel tijd er nog over is. Deze kan je aangeven in dagen, uren, minuten en seconde. Dit is ook makkelijk aan te passen. </p>

