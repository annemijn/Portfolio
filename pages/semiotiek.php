            <span>
            <h3>REFLECTIE</h3>
            </span>
        </div>

        <p>Ik vond dit een leerzame workshop. Ik weet nu wat semiotiek is en hoe je dit toepast op een poster of andere media. Het is namelijk heel belangrijk als media designer om media te maken die de doelgroep snapt. Ik weet nu ook dat beeld en typografie heel belangrijk is bij een poster. Hierdoor zal de doelgroep de betekenis van de poster sneller begrijpen als het goed is toegepast. Zelf zal ik de methode, betekenaar, betekende en betekenis vaker gaan gebruiken als ik media moet maken. Ik heb met deze opdracht ook geleerd hoe een posterserie in elkaar steekt. Ik denk dat ik nu anders naar posters ga kijken. Ik zal meer met een analytische blik de posters bekijken en waarnemen wat de betekenis achter zit en hoe ze met beeld hebben gewerkt.  </p>

        </p><br>

                 <div class="titeluitleg">
            <span>
            <h3>UITLEG</h3>
            </span>
            </div>
            
    	<div class="projecttext">
        <p>Met het vak SCO hebben we les gehad over semiotiek. De semiotiek houdt zich bezig met de leer van de tekens. In feite dus ook met de manier waarop de werkelijkheid wordt afgebeeld en hoe wij haar vastleggen. Met dat in ons achterhoofd zijn we gaan kijken naar verschillende afbeeldingen. Daarbij moesten we gaan nadenken over wat de betekenaar, betekende en uiteindelijk de betekenis van de afbeelding was. Betekenaar is wat je waarneemt (kleur, letters etc.) en betekende is waar het naar wordt verwijst. Daarna moesten we een poster uitzoeken en daarbij zelf de betekenaar, betekende en betekenis analyseren. Ik had deze poster uitgezocht:
</p>
        

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-item">
            <dt class="gallery-icon landscape">

        <img src="../images/postersemiotiek.jpg"class="attachment-thumbnail" height="548" width="958">
            </dt></dl>
        </div>

        <p><span>Betekenaar, wat neem ik waar?</span><br>
        Je ziet een mier op een leeg vlak waarbij staat “seen 9:53 pm” met een vinkje. Onder in het klein staat the Hyundai Santa Fe with Xenon Headlights. Nothing goes unseen.<br>
        <span>Betekende, waar verwijst het naar?</span><br>
        De mier verwijst naar iets wat klein is. De tijdstip en vinkje verwijst naar een berichtje op je telefoon. Met het vinkje wordt bedoeld dat het bericht is gekeken door het persoon waarnaar je het hebt gestuurd. <br>
        <span>Betekenis</span><br>
        Het betekent dat de Xenon headlights alles kan zien, zelfs een mier.
        </p>

        <p>Ik heb dit daarna samen met mijn docent overlegd. Samen kwamen we eruit dat we het een vage poster vonden. Het is niet gauw duidelijk waarom ze  voor de mier hebben gekozen. Dit is namelijk niet een belangrijke ding als je met een auto rijdt. Dit kan je nooit zien ook al heb je goede koplampen. Ze vroeg aan mij wat ik er zelf aan zal toevoegen om het duidelijker te maken. Ik vond het logischer om bijvoorbeeld een spotlight toe te voegen, zodat je weet dat het om een koplamp gaat die de mier ziet. </p>

        <p>voor een opdracht kregen we een aantal weken de tijd voor om een posterserie te maken. Hierbij moesten we rekening houden met de semiotiek en denken vanuit het merk, doelgroep, doelen, strategie en concept.<br>
        Ik ben meteen gaan kijken naar voorbeelden op internet. Wat me vooral opviel bij posterseries is dat de stijl bij elke poster het zelfde bleef en alleen een object of meerder objecten van de poster veranderde. Ik vond vooral posterserie heel sterk die minimalistisch bleven met niet teveel beeld. Waardoor het belangrijkst object van het poster opviel.</p>
       

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
            <dt class="gallery-icon landscape">

        <img src="../images/posterreeks.gif"class="attachment-thumbnail" height="548" width="958">
            </dt></dl>
        </div>

        <p>Daarna ben ik zelf gaan experimenteren met het maken van een posterserie. Met inspiratie van posters die ik had opgezocht ben ik zelf een poster gaan maken. Hierbij wilde ik de activiteiten die je bij het festival kan doen als belangrijkste neerzetten op de poster. 
        </p>

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
            <dt class="gallery-icon landscape">

        <img src="../images/postersemiotiek1.png"class="attachment-thumbnail" height="548" width="958">
            </dt></dl>
        </div>

        <p>Hierbij heb ik als eerst ook de betekenaar, betekende en betekenis toegepast. Zo krijg je een idee of het duidelijk is voor degene die naar de poster gaat kijken.
        <p> 

        <p><span>Betekenaar, wat neem ik waar?</span><br>
        Je ziet een hangmat met palmbomen op een paarse achtergrond. Daaromheen staan muzieknoten. Boven staat er in het groot “Dolce far niente”met daaronder in een vakje chill festival. Onder de hangmat zie je 5-08-2017 en de website van dolce far niente. Daaronder is een logo van het gelders orkest.<br>
        <span>Betekende, waar verwijst het naar?</span><br>
        De hangmat met palmbomen verwijst naar chillen, relaxen, vakantie, zomer. De muzieknoten verwijzen naar muziek. De patroon in de hangmat verwijzen naar natuur, zon, warmte, chillen en muziek. Chill festival verwijst naar dat het een chille festival is. 5-08-2017 verwijst naar de datum. Het gelders orkest logo verwijst naar klassieke muziek.<br>
        <span>Betekenis</span><br>
        Dolce far niente is een festival dat zich richt op chillen. Dit kan je zien door de hangmat en de tekst die erbij is gezet. Verder is de festival op 5 augustus 2017. Het Gelders Orkest zal ook aanwezig zijn.
        </p>

        <p>Het <span>beeld</span> wilde ik de activiteit, hier is dat de hangmat, het meest laten opvallen. Dit deed ik door de hangmat in het midden te plaatsen en genoeg ruimte te geven. Ook maakte ik gebruik van een effe achtergrond. Hierdoor viel de hangmat op. Ik heb muzieknoten links en rechts van de hangmat gezet. Hierdoor krijg je een idee dat er muziek wordt afgespeeld. in de hangmat er is een patroon gezet. Hiermee wilde ik de sfeer van het concept laten zien. De bomen staan voor de natuur en rust, de zon en zonnebril staan voor warmte en zomer en de muzieknoot voor de muziek. Onderaan heb ik minder opvallen het logo van het Gelders Orkest neergezet. Hiermee wil ik een hint geven dat het Gelders Orkest bij het festival gaat optreden. </p>

        <p>Voor de <span>typografie</span> heb ik een aantal lettertypes gebruikt. Ik wilde bij deze poster dat de naam van het festival het meest moest opvallen. Daarom heb ik gebruik gemaakt van een dikke lettertype. Deze tekst heb ook het grootst gemaakt. De titel heb ik bovenaan geplaatst, omdat je dit het eerst zal gaan lezen. Daaronder heb ik “chill festival” neergezet. Hierdoor krijg je een beter beeld wat dolce far niente inhoudt. Dit is de <span>short copy</span>, omdat ik hiermee de lezer wil overtuigen. Omdat dit niet zo belangrijk is als de titel, heb ik dit kleinere letters gegeven en een minder dikke lettertype. Dit is wel de tweede belangrijkste tekst en heb ik hieromheen een vak gezet. Onder de hangmat heb ik de datum en de website van het festival neergezet. Dit zal je nadat je de bovenste tekst en afbeelding heb gezien bekijken. Dit heb ik hier neergezet, omdat de datum en de website pas belangrijk is als je meer informatie weet over het festival. Je weet nu dat het een chill festival is en wat de sfeer van het festival is. De website zal je hierdoor gaan bezoeken.</p>

        <p>Hierna heb ik de posterserie gemaakt. Hierbij is bij elke poster de activiteit verschillend en is de achtergrondkleur anders. </p>


