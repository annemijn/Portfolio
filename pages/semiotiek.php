        <p>Als <span>concept</span> willen wij de jongeren bereiken door een speciaal relaxt openluchtfestival te organiseren met Het Gelders Orkest als hoofdact. We hebben ervoor gekozen om als openluchtfestival neer te zetten omdat de <span>doelgroep</span> van mening is dat de klassieke opzet van een klassiek concert geen bal aan is. De festivalsfeer is gekend en geliefd. Daarom heb ik de posters een festivalsfeer gegeven. Daarbij heb ik gebruik gemaakt van verschillende wat fellere kleuren. Ook heb ik de sfeer van een festival in de patroon van het object neergezet. het object (drankje, hangmat, bbq etc) geeft ook het festivalsfeer weer. Daarbij heb ik lettertypes gebruikt die strak zijn. Dit wordt vaak bij festivals gebruikt. Met deze posters wil ik het merk onder aandacht brengen bij de doelgroep. Dit is onze <span>strategie</span>. Door te laten zien wat dolce far niente is en wat voor activiteiten er te doen zijn, creëer je awareness. </p>



