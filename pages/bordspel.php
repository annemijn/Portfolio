            <h3>REFLECTIE</h3>
            </span>
        </div>

        <p>Ik heb veel geleerd van deze workshop. Zo weet ik nu wat Google Material Design is en kan ik het ook toepassen op een design. Ik denk dat dit erg handig is voor later als een opdrachtgever bijvoorbeeld vraagt of ik iets wil maken met daarbij Google Material Design. Ik merk dat Google Material Design erg populair is. Zelf vind ik Google Material Design ook een heel mooi design. Dit zal ik wel vaker als inspiratie meenemen in designs. Ook weet ik nu hoe je een interaction Design kan maken en wat voor tools hiervoor beschikbaar zijn. Ik weet nu hoe de tool proto.io werkt en wat je ermee kan doen. Verder is de tool erg makkelijk te gebruiken en je kan heel snel prototypes maken. Dit is handig voor later als ik een opdracht heb om een interactief prototype te maken.</p><br>

                 <div class="titeluitleg">
            <span>
            <h3>UITLEG</h3>
            </span>
            </div>
            
    	<div class="projecttext">
        <p>Met de workshop prototyping zijn we bezig geweest met interaction design. Dit is het bewust en gericht vormgeven van de GUI en de interactie. Hierbij gaat het om het structuren en organiseren van elementen en visueel ontwerpen van de elementen. We kregen een uitleg over Google Material Design. Daarbij heb ik nog een klein onderzoek gedaan van hoe dit er grafisch uit ziet: </p>
        
        <p>- Het combineren van skeuomorfisme en flat design. Een lichte schaduw onder een platte knop, zouden ervoor zorgen dat onderdelen in een interface beter kunnen worden onderscheiden van elkaar.</p>

        <p>-De introductie van (design)fysica: door diepte te creëren door middel van onder andere intuïtieve animaties met diepte. Denk hierbij aan objecten die naar de voorgrond lijken te komen wanneer deze worden gesleept terwijl de rest vervaagt.</p>

        
        <p>Door dit onderzoek weet ik hoe je Google Material Design kan toepassen. Hierna kregen we de opdracht om een traditioneel bordspel uit te kiezen en hiervan een digitale variant voor de iPad te ontwikkelen. Ze raadde ons wel aan om een prototype voor je concept te maken i.p.v. een bordspel. Omdat ik eerst wilde oefenen heb ik gekozen om eerst een bordspel te maken. Ik heb hiervoor het bordspel Mens Erger Je Niet gekozen. Als eerst heb ik een schets gemaakt. </p>

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
            <dt class="gallery-icon landscape">

        <img src="../images/bordspelv1.jpg"class="attachment-thumbnail" height="548" width="958">
            </dt></dl>
        </div>

        <p>Daarna ben ik begonnen met het maken van de prototype. Ik heb gebruik gemaakt van proto.io waarmee je interactieve apps kan maken. Hierbij heb ik de regels aangehouden van Google Material Design. Zo heb ik onder de knoppen een lichte schaduw gegeven zodat je de onderdelen van de interface goed kan onderscheiden. Het design heb ik ook simpel gehouden. Ook heb ik gebruik gemaakt van intuïtieve animaties bij het kiezen van een speler. Hierdoor lijkt het net alsof het naar de voorgrond komt.</p>
        
        <p>Hierna heb ik ook nog drie usertests uitgevoerd bij mijn prototype. Daarbij hebben ze mijn prototype doorgelopen. Ze wisten allemaal hoe ze het spel moesten starten en een kleur moesten kiezen. Sommige wisten daarna niet zo goed wat ze moesten doen. Ik had toen nog niet bij de dobbelsteen een tekstvak met daarop “gooi!” toegevoegd. Door dit erbij toe te voegen wisten ze toen wel hoe je het spel kon spelen. Ze vonden het erg fijn dat je bij het kiezen van een kleur het object naar voren kwam. Wat iemand nog opmerkte is dat ik niet genoeg rondjes op het bordspel had, dit waren er 4 in plaats van 5. Dit heb ik daarna verbeterd.
        </p>

