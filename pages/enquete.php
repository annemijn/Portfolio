        <p>Deze gebruikersonderzoek heeft me goed geholpen om meer inzicht te krijgen in de doelgroep. Door deze resultaten kunnen wij aantonen dat de jongeren dit concept leuk vinden. Ik vind een enquête als gebruikersonderzoek heel waardevol. Door het op Facebook te verspreiden krijg je genoeg reacties en hiermee kan je ook makkelijker conclusies trekken. Ik vond het fijn om met deze gebruikersonderzoek te werken, want we hebben veel informatie over de doelgroep gekregen. Met een enquête kan je heel gemakkelijk antwoord krijgen van je doelgroep. Ik merk wel dat dit niet altijd heel betrouwbaar is. Je weet namelijk niet of diegene het serieus heeft ingevuld en of iemand het meerdere keren heeft gedaan. Om het wat persoonlijker en betrouwbaarder te maken zal ik kiezen voor interviews. </p>

                 <div class="titeluitleg">
            <span>
            <h3>UITLEG</h3>
            </span>
            </div>
            
    	<div class="projecttext">
        <p>Voor het concept hebben we een enquête rondgestuurd bij onze doelgroep. De enquête hebben we gemaakt met Google Forms. Hierbij hebben we een aantal vragen opgesteld die te maken hebben of jongeren stress ondervinden. Daarnaast hebben we ook onze festival voorgelegd. Daarbij hebben we gevraagd of ze deze festival willen bezoeken. </p>

        <div id="gallery-3" class="gallery galleryid-2178 gallery-columns-3 gallery-size-thumbnail"><div class="clearboth"></div><dl class=" gallery-fullwidth">
            <dt class="gallery-icon landscape">

        <img src="../images/enquete.png"class="attachment-thumbnail" height="548" width="958">
            </dt></dl>
        </div>

        <p>Ik heb de enquête gedeeld via Facebook waarbij ik veel mensen van de doelgroep kan aanspreken. We hebben de enquête een aantal dagen op Facebook laten staan, zodat er genoeg mensen de enquête konden invullen. Uiteindelijk hebben er 130 mensen de enquête ingevuld. Daarna hebben we de resultaten van elke vraag in een diagram gezet. Zo kan je duidelijk zien in procenten wat het hoogst scoort. Bijvoorbeeld bij de vraag “zou je deze festival bezoeken?” had er 38,9% ja gezegd, 22,1% nee en 38,9% misschien. Dit is voor ons heel erg waardevol, want we hebben al 39% die willen komen en hoeven we nog maar 39% over te halen.</p>

