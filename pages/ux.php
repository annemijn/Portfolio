        <p>Met deze opdracht was het leerzaam om te onderzoeken wat een UX-designer op een dag doet. Door de activiteiten op te splitsen in wat ik al heb gedaan en nog niet, weet ik wat ik als UX Designer kan en waar ik me nog kan in ontwikkelen. Ik zag dat ik al best veel activiteiten van een UX-designer had gedaan. Er zijn nog best veel activiteiten waar ik nog nooit van gehoord had. Door een aantal UX-activiteiten te onderzoeken weet ik nu ook wat deze activiteiten inhouden. Ik hoop dat ik een aantal van deze nog tegenkom in mijn studie.</p><br>

                 <div class="titeluitleg">
            <span>
            <h3>UITLEG</h3>
            </span>
            </div>
            
    	<div class="projecttext">
        <p>Met deze workshop kregen we uitleg over User Experience. User Experience is "Everything that affects a user's interaction with a product". We kregen uitleg over wat een experiment is bij user experience. Daarbij moesten we zelf een experiment verzinnen voor ons concept. </p>
        
        <p><span>Vraag: </span><br>
        Zal de doelgroep via Facebook op de advertentie klikken om naar de website te gaan?</p>

        <p><span>Doen: </span><br>
        Kijken of er mensen op de advertentie klikken d.m.v. het overzicht van Facebook advertenties. Verschillende advertenties maken waardoor je kan zien welke advertentie het meeste aantrekt. Op visueel gebied. </p>
        
        <p><span>Hoelang: </span><br>
        Dit doe je voor ongeveer een week. Hierdoor krijg je een breed beeld over de kliks op de advertenties. </p>

        <p>Daarna moesten we gaan beschrijven waarom je een goede UX Designer bent en hoe je jezelf gaat ontwikkelen om een nog betere UX Designer te worden. <br>
        Om meer te weten te komen wat een UX Designer doet, ben ik gaan kijken uit welke activiteiten het werk van een UX-designer bestaat. Ik ben toen gaan zoeken op internet. Daar kwam ik uit bij een bron: <a href="https://uxdesign.cc">https://uxdesign.cc</a>. Met deze website ben ik activiteiten van een UX Designer te weten gekomen. De activiteiten heb ik verdeeld in UX-activiteiten die ik heb gedaan en acitiviteiten die ik (nog) niet heb gedaan. Zo kom ik te weten wat ik als UX Designer al kan.</p>

        <p><span>UX-activiteiten die ik heb gedaan:</span><br>
            -   User Testing <br>
            -   User stories<br>
            -   Personas maken<br>
            -   Scenarios en storyboards<br>
            -   Brainstorming<br>
            -   Moodboards<br>
            -   User flow<br>
            -   Usability test<br>
            -   Eyetracking <br>
            -   Schetsen<br>
            -   Prototypes </p>

        <p>Deze verschillende activiteiten heb ik op school toegepast op verschillende opdrachten. Zo kan ik bijvoorbeeld al goed storyboards en moodboards maken, omdat ik hiermee vaak heb geoefend. Ook heb ik voor opdrachten personas moeten maken en daarbij de user stories, user flow en user testing toegepast. Dit was bij opdrachten zoals proftaak. Ik heb al heel vaak moeten brainstormen met groepsopdrachten. Dit is daarom ook een activiteit die ik al goed ken. Schetsen is een van de activiteiten die ik erg leuk vind om te doen en gebruik ik vaak bij opdrachten, zoals met logoâ€™s en websites ontwerpen.</p>

        <p><span>UX-activiteiten die ik niet heb gedaan:</span><br>
            -   Consumer Journey Map<br>
            -   Ecosystem Map<br>
            -   Competitive Audit<br>
            -   Blueprint<br>
            -   Stakeholders Interviews<br>
            -   Value Proposition<br>
            -   Key Performance Indicators<br>
            -   Task Analysis<br>
            -   Card Sorting<br>
            -   A/B Test<br>

        <p>Deze verschillende activiteiten heb ik nooit gedaan of nog nooit van gehoord. Sommige activiteiten lijken mij handig voor een opdracht. Bijvoorbeeld de A/B Test. Je maakt twee versies van een product en zet dit online. Hierna vergelijk je de resultaten van allebei en zie je welke beter scoort. Hiermee weet je meteen welke je gaat gebruiken. Dit lijkt mij vooral handig met websites of apps. The consumer journey map lijkt me ook handig. Zo zie je wat de klant voor stappen onderneemt van de service of product. Hiermee kan je dan goed op inspelen en kan je de motivatie van de klant verhogen. Dit lijkt me erg handig voor proftaak opdrachten. Task analysis komt me bekend voor alleen heb ik nog nooit toegepast op een opdracht. Hierbij gaat het om hoe een opdracht is uitgevoerd en wat voor activiteiten hierbij zijn toegepast. Dit lijkt mij het beste om te gebruiken bij het testen van een product.</p>

