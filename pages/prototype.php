            <h3>REFLECTIE</h3>
            </span>
        </div>

        <p>Ik vond het interessant om met een nieuwe prototype tool te werken. Ik weet nu hoe Adobe Experience Design werkt. Ik kan dit nu gaan gebruiken voor als ik het nodig heb in de toekomst. Vergeleken met de andere twee prototype tools waarmee ik heb gewerkt, vind ik dat deze tool een stuk ingewikkelder in elkaar zit. Je hebt heel veel opties en je moet zelf heel het scherm gaan maken met vakjes en tekstblokken. Ik vind deze tool wel een stuk professioneler. Je kan er hele mooie prototypes mee maken die er professioneel uit zien. Wat ik ook fijn vind is dat je de prototype ook kan testen op je mobiel. Dit is vooral heel fijn omdat ik dus een mobiele app prototype maakte. </p><br>

                 <div class="titeluitleg">
            <span>
            <h3>UITLEG</h3>
            </span>
            </div>
            
    	<div class="projecttext">
        <p>Voor UX zijn we een aantal prototypes gaan maken voor ons concept. Zo kwamen we erop om een armband te gaan gebruiken voor de festival. Bij binnenkomst op het festival krijgen de bezoekers een polsbandje. Het is de bedoeling dat dit bandje de bezoekers de mogelijk biedt om onder andere muziek met een druk toe te voegen aan hun eigen playlist. Via een app kan de bezoeker bijhouden hoeveel muntjes voor consumpties er op het bandje staan. Daarnaast is het bandje ook een leuk aandenken aan het festival.</p>

        <p>Met behulp van een paper prototype willen we de gebruiker fysiek laten ervaren hoe het is om een polsbandje te dragen. Daarnaast willen we hiermee ook testen of het voor de gebruiker duidelijk is wat de knop is op het bandje. Tevens willen we dit bandje ook gebruiken tijdens de video / roleplay prototyping om zo een volledige ervaring voor de bezoeker te creÃ«ren.</p>

        <p>Voor het armbandje heb ik als prototype de app voor het armbandje gemaakt. Dit heb ik gedaan met Adobe Experience Design. Het leek me wel leerzaam om een nieuwe prototype tool te gebruiken. Met Adobe Experience Design heb ik namelijk nog nooit mee gewerkt. Als eerst ging ik een makkelijke tutorial volgen op YouTube. Hij legde duidelijk uit welke dingen je moest weten om een eenvoudige prototype te maken. Ik heb voor deze prototype een avond aan gewerkt om een simpele maar werkende prototype te krijgen. Het is voor mobiel en je kan hem ook op je mobiel laten testen. </p>

