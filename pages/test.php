            <h3>REFLECTIE</h3>
            </span>
        </div>

        <p>Ik vond het leerzaam om een usabillity test uit te voeren voor onze website. Wat ik heel fijn vind aan usabillity testing is dat er eerst een testplan wordt opgesteld. Hiermee kan je goed de test uitvoeren. Verder vond ik het ook fijn dat er iemand observeert en iemand notuleert. Hierdoor heb je meerdere mensen die de test analyseren en kan je meer informatie uiteindelijk krijgen. Ik vond het ook fijn om de verschillende rollen te zijn. Als je degene bent die de vragen stelt, merk je dat je vooral bezig bent met het luisteren naar de persoon, terwijl als je notuleert je ook meer kijkt wat voor handelingen hij doet. Daarom zijn deze twee rollen erg belangrijk om te hebben bij een usabillity test. Ik vond het ook fijn om vragen te stellen waardoor ik me meer in het interviewen kon verbeteren. Ik liet de testpersoon zijn gang gaan bij de scenario’s en liet vooral niet mijn mening horen. Ook bleef ik even stil zodat hij kon nadenken over de antwoord die hij wilde geven. Verder waren de resultaten van de test heel nuttig voor onze website. Hierdoor kunnen we het weer gaan verbeteren.
        </p><br>

                 <div class="titeluitleg">
            <span>
            <h3>UITLEG</h3>
            </span>
            </div>
            
    	<div class="projecttext">
        <p>Voor de usabillity test wilde we onze website gaan testen. Hiervoor moest er eerst een testplan voor worden gemaakt. Daarvoor heb ik de moderating techniek uitgeschreven. We wilde gebruik maken van Think Aloud. voordat we de test starten vragen we aan de testpersonen of ze hardop willen denken, zodat we weten welke handelingen ze waarom doen. Dit helpt ons om een beeld te krijgen van de denk / handelswijze van onze testpersonen. Daarna heb ik de inleiding geschreven. Hierin heb ik onze doel uitgelegd van de usabillity test. Daarna zijn we met de groep de vragen en scenario’s gaan bedenken. Elke scenario bestond uit een vraag, taak en scenario. Daarnaast hadden we nog extra vragen die we op het laatst wilde stellen. De testplan kan je <a href="../Testplan.pdf">hier</a> lezen </p>

        <p>Bij de eerste testpersoon had ik als rol die de vragen stelde. Bij elke scenario/vraag gaf ik hem voldoende tijd om het uit te voeren, zodat we genoeg informatie kregen en niet door mij de resultaat werd beïnvloed. Hierbij merkte ik dat ik mezelf vooral in de achtergrond moest treden en hem zelf alles laten doen. bij twee andere testpersonen heb ik ook genotuleerd. Hierbij merkte ik dat ik vooral de handelingen die de testpersoon goed moest bijhouden. Dit was namelijk heel belangrijk om op te schrijven.  </p>

        <p>Ik vond dat deze usabillity test heel nuttig is bij het verbeteren van de website. Waarbij er vooral de gebruiksvriendelijkheid verbeterd kan worden. Zo was bijvoorbeeld de locatie op google maps niet handig te lezen en wilde de meeste testpersonen meer informatie over deze locatie hebben. Bijvoorbeeld een routebeschrijving. </p>
        

