    	<div class="projecttext">
        <p>Voor de website van het concept heb ik een prototype gemaakt. Dit is door ieder groepslid individueel gedaan. Zo kunnen we elkaars prototype vergelijken en uiteindelijk een website uitkiezen of combineren. Als eerst ben ik de indeling van de website gaan verzinnen. Ik wilde graag een grote header die veel aandacht trekt met de logo van het concept erop. Daarna kan je op het pijltje klikken om meer informatie erover te zien. Verder wilde ik graag een count down van het evenement laten zien. Op internet zag ik dit vaker voorkomen bij evenementen. Het geeft een gevoel dat het een echte evenement is wat goed is uitgedacht. Daarna wilde ik een duidelijke maar cleane gedeelte hebben waar je op de knop kan klikken om tickets te kopen. Hierbij wilde ik een opvallende button met een bijpassende achtergrond. Ook wilde ik een gedeelte hebben waar je kan zien wat voor activiteiten er aanwezig zullen zijn. Hierbij wilde ik dat de sfeer beelden het duidelijk aangaven om wat voor activiteit het gaat. Als je eroverheen gaat met je muis krijg je te zien welke activiteit er is. Verder wilde ik als laatst een footer en een fixed menu bovenaan. </p>
        
        <p>Deze indeling begon ik in Photoshop te schetsen. Daarbij moest ik nog nadenken over de kleuren en lettertype die ik wilde gebruiken op de website. Ik koos voor de achtergrond een opvallende roze kleur.  Ik vond dit goed bij de logo passen, omdat er tinten roze in terug kwam. Nadat ik het ontwerp had gemaakt in Photoshop ging ik het interactief maken.</p>

