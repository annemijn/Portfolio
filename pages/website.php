        <p>Om een prototype te maken wilde ik een nieuwe prototype tool gebruiken. Voor mijn prototype van het bordspel maakte ik gebruik van de tool proto.io. De docent raadde ons een aantal andere tools aan, waaronder Invision. Voor de Invision tool kregen we een gratis premium account als student. Deze tool probeerde ik daarom uit. Ik vond de tool op het begin lastig te gebruiken. Ik wist niet hoe je moest beginnen. Je kon alleen een Photoshop bestand aanleveren. Zelf kon je niet een scherm zelf helemaal maken. Omdat ik het niet helemaal begreep volgde ik een tutorial. Dit maakte het een stuk duidelijker en ik wist hoe je moest beginnen met het maken van een prototype. Ik zette mijn ontwerp die ik had gemaakt erop. Daarna kon ik in de build mode hotspots creÃ«ren. Deze hotspots gaven aan dat je erop kon klikken of hoveren en dat je daarbij een actie kan uitvoeren. Dit ging makkelijk. Zo kon ik een menu link een actie uitvoeren om naar een bepaalde hoogte te gaan. Verder kon ik de menu als fixed header zetten, zodat deze op dezelfde plek bleef staan als je scrolde. Om over een afbeelding een kleur overlay te plaatsen was wel moeilijk. Je kon niet zelf een overlay in de tool maken. Dit moest ik dus in Photoshop doen. Daarna kon ik dit plaatje over de andere afbeelding plaatsen. hieronder zie je de werkende prototype van de website.</p>

